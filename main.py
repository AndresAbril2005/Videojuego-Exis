def mover(dx, dy, direccion):

    if mostrar_minimapa or mostrar_ranking:
        return
    
    global jugador_x
    global jugador_y
    global jugador_img_actual
    global nivel_actual
    global laberinto
    global mapa_descubierto

    nx = jugador_x + dx
    ny = jugador_y + dy

    # No atravesar paredes
    if laberinto[ny][nx] != 1:

        jugador_x = nx
        jugador_y = ny

        descubrir_alrededor()

        if not canal_pasos.get_busy():
            canal_pasos.play(paso_sonido)

    jugador_img_actual = jugador_imgs[direccion]

    # Llegó a la meta
    if laberinto[jugador_y][jugador_x] == 2:

        pygame.mixer.music.stop()

        reproducir_cinematica()

        nivel_actual += 1

        actualizar_ranking()

        # Hay siguiente nivel
        if nivel_actual in laberintos:

            laberinto = laberintos[nivel_actual]

            # Reiniciar el minimapa
            mapa_descubierto = [
                [False for _ in fila]
                for fila in laberinto
            ]

            cargar_texturas()

            jugador_x = 1
            jugador_y = 1

            # Descubrir la zona inicial del nuevo nivel
            descubrir_alrededor()

            pygame.mixer.music.play(-1)

        else:

            messagebox.showinfo(
                "GANASTE",
                "Completaste todos los niveles"
            )

            ventana.destroy()








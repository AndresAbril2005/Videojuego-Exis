def dibujar():
    
    global salida_index

    canvas.delete("all")

    # Tamaño ventana
    ancho_pantalla = ventana.winfo_width()
    alto_pantalla = ventana.winfo_height()

    # Celdas visibles
    columnas_visibles = ancho_pantalla // TAM_CELDA
    filas_visibles = alto_pantalla // TAM_CELDA

    # Cámara
    cam_x = jugador_x - columnas_visibles // 2
    cam_y = jugador_y - filas_visibles // 2

    # Limitar cámara
    cam_x = max(
        0,
        min(cam_x, len(laberinto[0]) - columnas_visibles)
    )

    cam_y = max(
        0,
        min(cam_y, len(laberinto) - filas_visibles)
    )

    # Dibujar mapa
    for y in range(filas_visibles):
        for x in range(columnas_visibles):

            mapa_x = x + cam_x
            mapa_y = y + cam_y

            if mapa_y >= len(laberinto):
                continue

            if mapa_x >= len(laberinto[0]):
                continue

            distancia = abs(mapa_x - jugador_x) + abs(mapa_y - jugador_y)

            px = x * TAM_CELDA
            py = y * TAM_CELDA

            # Oscuridad
            if distancia > VISION:

                canvas.create_rectangle(
                    px,
                    py,
                    px + TAM_CELDA,
                    py + TAM_CELDA,
                    fill="black",
                    outline="black"
                )

                continue

            valor = laberinto[mapa_y][mapa_x]

            # Pared
            if valor == 1:

                canvas.create_image(
                    px,
                    py,
                    image=pared_img,
                    anchor="nw"
                )

            # Meta
            elif valor == 2:

                canvas.create_image(
                    px + TAM_CELDA // 2,
                    py + TAM_CELDA // 2,
                    image=salida_imgs[salida_index],
                    anchor="center"
                )

            # Piso
            else:

                canvas.create_image(
                    px,
                    py,
                    image=piso_img,
                    anchor="nw"
                )

    # Dibujar jugador
    jugador_pantalla_x = (
        (jugador_x - cam_x) * TAM_CELDA
        + TAM_CELDA // 2
    )

    jugador_pantalla_y = (
        (jugador_y - cam_y) * TAM_CELDA
        + TAM_CELDA // 2
    )

    canvas.create_image(
        jugador_pantalla_x,
        jugador_pantalla_y,
        image=jugador_img_actual,
        anchor="center"
    )


    if mostrar_ranking:

        # =========================
        # RANKING
        # =========================

        canvas.create_text(
            ranking_x,
            ranking_y,
            anchor="nw",
            fill="gold",
            font=("Arial",18,"bold"),
            text="🏆 RANKING"
        )

        y = ranking_y + 35

        puesto = 1
        print("Ranking:", cliente.ranking)
        for jugador in cliente.ranking:

            minutos = jugador["tiempo"] // 60
            segundos = jugador["tiempo"] % 60

            texto = (
                f"{puesto}. {jugador['nombre']}\n"
                f"Nivel {jugador['nivel']}   {minutos:02}:{segundos:02}"
            )

            canvas.create_text(
                ranking_x,
                y,
                anchor="nw",
                fill="white",
                font=("Arial",14),
                text=texto
            )

            y += 45

            puesto += 1







    # Animación salida
    salida_index = (
        salida_index + 1
    ) % len(salida_imgs)

    dibujar_minimapa()
    ventana.after(90, dibujar)








def dibujar_minimapa():

    if not mostrar_minimapa:
        return

    tam = 8

    inicio_x = ventana.winfo_width() - len(laberinto[0]) * tam - 20
    inicio_y = 20

    for y in range(len(laberinto)):
        for x in range(len(laberinto[0])):

            if not mapa_descubierto[y][x]:
                color = "black"

            else:

                if laberinto[y][x] == 1:
                    color = "gray"

                elif laberinto[y][x] == 2:
                    color = "gold"

                else:
                    color = "white"

            canvas.create_rectangle(
                inicio_x + x * tam,
                inicio_y + y * tam,
                inicio_x + (x + 1) * tam,
                inicio_y + (y + 1) * tam,
                fill=color,
                outline=""
            )

    canvas.create_rectangle(
        inicio_x + jugador_x * tam,
        inicio_y + jugador_y * tam,
        inicio_x + (jugador_x + 1) * tam,
        inicio_y + (jugador_y + 1) * tam,
        fill="red",
        outline=""
    )










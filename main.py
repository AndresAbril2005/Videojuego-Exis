def tecla(evento):

    global mostrar_minimapa

    tecla = evento.keysym

    # Abrir/Cerrar minimapa
    if tecla.lower() == "m":
        mostrar_minimapa = not mostrar_minimapa
        return
    
    if tecla == "Tab":
        global mostrar_ranking
        mostrar_ranking = not mostrar_ranking
        return "break"

    # Si el minimapa está abierto, no permitir movimiento
    if mostrar_minimapa:
        return

    if tecla == "Up":
        mover(0, -1, "up")

    elif tecla == "Down":
        mover(0, 1, "down")

    elif tecla == "Left":
        mover(-1, 0, "left")

    elif tecla == "Right":
        mover(1, 0, "right") 

ventana.bind("<Key>", tecla)










